// write a script that perform basic operation (addition, multiplication, subtraction, dividion, remainder) on two numbers and log the result.

function arithmeticOper(num1, num2) {
  
  console.log(`the additon of two numbers ${num1}, ${num2} is ` + (num1 + num2));
  console.log(`the additon of two numbers ${num1}, ${num2} is ` + (num1 * num2));
  console.log(`the additon of two numbers ${num1}, ${num2} is ` + (num1 - num2));
  console.log(`the additon of two numbers ${num1}, ${num2} is ` + (num1 / num2));
  console.log(`the additon of two numbers ${num1}, ${num2} is ` + (num1 % num2));
}

arithmeticOper(20, 10);
